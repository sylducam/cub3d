#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char		*cpy;
	size_t		i;

	i = 0;
	cpy = wrmalloc(sizeof(char) * (ft_strlen(s1) + 1));
	if (cpy == NULL)
		return (NULL);
	while (s1[i])
	{
		cpy[i] = s1[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
